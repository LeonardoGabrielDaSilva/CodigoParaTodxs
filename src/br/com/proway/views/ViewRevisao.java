    public Pessoa getPessoa() {
        return pessoa;
    }

    public void setPessoa(Pessoa pessoa) {
        this.pessoa = pessoa;
    }

    /**
     * Completa os campos de informações e área do contrato com os dados
     * adquiridos no processo
     */
    private void populaCampos() {
        txtNome.setText(pessoa.getNome());
        txtIdade.setText(pessoa.getIdade());
        txtCpf.setText(pessoa.getCpf());
        if (pessoa.getCpfNegativado()) {
            txtCpfNegativado.setText("Sim");
        } else {
            txtCpfNegativado.setText("Não");
        }
        txtEmail.setText(pessoa.getEmail());
        txtCelular.setText(pessoa.getCelular());
        txtEscolaridade.setText(pessoa.getEscolaridade());
        txtOcupacao.setText(pessoa.getOcupacao());
        txtQuantidadeFilhos.setText(Integer.toString(pessoa.getQntdFilhos()));
        txtRendaMensal.setText(pessoa.getRendaMensal());
        txtMotivoEmprestimo.setText(pessoa.getMotivoEmprestimo());

        Endereco endereco = pessoa.getEndereco();

        txtCep.setText(endereco.getCep());
        txtCidade.setText(endereco.getLocalidade());
        txtBairro.setText(endereco.getBairro());
        txtEstado.setText(endereco.getUf());
        txtLogradouro.setText(endereco.getLogradouro());
        txtNumero.setText(Integer.toString(endereco.getNumeroCasa()));

        Contrato contrato = pessoa.getContrato();

        areaContrato.append("Valor de Empréstimo: R$" + contrato.getValor());
        areaContrato.append("\nQuantidade de parcelas: " + contrato.getParcelas());
        areaContrato.append("\nValor das parcelas: R$" + contrato.calculaValorParcelaComJuros());
        areaContrato.append("\nValor de juros mensal: " + contrato.getPorcentagemJuros() + "%");

    }

    /**
     * Finaliza a aplicação
     */
    private void finalizaAplicacao() {
        System.exit(0);
    }

    /**
     * Volta à tela inicial e recomeça a aplicação
     */
    private void reiniciaAplicacao() {
        ViewEmprestimo tela = new ViewEmprestimo();
        tela.setVisible(true);
        dispose();
    }

