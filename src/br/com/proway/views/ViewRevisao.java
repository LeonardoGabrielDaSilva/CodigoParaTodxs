    private void btnContratacaoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnContratacaoActionPerformed
        if (JOptionPane.showConfirmDialog(null, "Tem certeza que deseja confirmar?", "Realizar Contrato", JOptionPane.YES_NO_OPTION) == 0) {
            ImportarExportar exp = new ImportarExportar();
            List<Pessoa> lista = new ArrayList<>();
            try {
                exp.importarPessoa(lista);
                lista.add(pessoa);
                exp.exportarPessoa(lista);
            } catch (IOException ex) {
                Logger.getLogger(ViewRevisao.class.getName()).log(Level.SEVERE, null, ex);
            }
            JOptionPane.showMessageDialog(null, "Contrato realizado com Sucesso!", "Contrato confirmado", JOptionPane.INFORMATION_MESSAGE);
            finalizaAplicacao();
        }
    }//GEN-LAST:event_btnContratacaoActionPerformed

